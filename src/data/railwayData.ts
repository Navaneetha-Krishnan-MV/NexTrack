  // Get warranty status
  export const getWarrantyStatus = (warrantyExpiry: string) => {
    try {
      const today = new Date();
      today.setHours(0, 0, 0, 0); // Normalize to start of day
      
      const [month, year] = warrantyExpiry.split('-').map(Number);
      const expiryDate = new Date(2000 + year, month - 1, 1); // Set to first day of expiry month
      
      // Set to last day of the expiry month
      expiryDate.setMonth(expiryDate.getMonth() + 1);
      expiryDate.setDate(0);
      expiryDate.setHours(23, 59, 59, 999); // End of the last day of the month
      
      const diffTime = expiryDate.getTime() - today.getTime();
      const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));
      
      if (diffDays < 0) return { status: 'expired', color: 'destructive', text: `Expired on ${expiryDate.toLocaleDateString('en-US', { month: 'long', year: 'numeric' })}` };
      if (diffDays <= 90) return { status: 'expiring', color: 'warning', text: `Expires in ${diffDays} days (${expiryDate.toLocaleDateString('en-US', { month: 'long', year: 'numeric' })})` };
      return { status: 'valid', color: 'success', text: `Valid until ${expiryDate.toLocaleDateString('en-US', { month: 'long', year: 'numeric' })}` };
    } catch (error) {
      console.error('Error calculating warranty status:', error);
      return { status: 'unknown', color: 'muted', text: 'Warranty status unknown' };
    }
  };
  
